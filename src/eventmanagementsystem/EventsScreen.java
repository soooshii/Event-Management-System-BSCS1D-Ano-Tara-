    private void loadEventsTable() {
        try {
            Connection conn = DatabaseConnection.getConnection();
            PreparedStatement stmt = conn.prepareStatement("SELECT * FROM events");
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblEvents.getModel();
            model.setRowCount(0); // Wipes the table clean before loading

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("event_id"),
                    rs.getString("event_name"),
                    rs.getDate("event_date"),
                    rs.getString("venue"), 
                    rs.getInt("max_slots")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading table: " + e.getMessage());
        }
    }
