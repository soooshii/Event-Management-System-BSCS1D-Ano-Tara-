    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // 1. Check if an event was actually selected from the table
        // We use the class-level selectedEventId tracker here!
        if (selectedEventId == 0) {
            JOptionPane.showMessageDialog(this, "Please click on an event in the table first to delete it.");
            return; // Stop the code
        }

        // 2. Ask for confirmation (Safety first!)
        int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to completely delete this event?", 
                "Confirm Delete", 
                JOptionPane.YES_NO_OPTION);

        // 3. If they click "Yes", execute the SQL
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection conn = (Connection) DatabaseConnection.getConnection();
                
                // The SQL command to delete based on the ID
                String sql = "DELETE FROM events WHERE event_id = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setInt(1, selectedEventId); // Uses your clicked ID!
                
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Event Successfully Deleted!");
                
                // 4. Refresh the table and reset the tracker
                loadEventsTable();
                selectedEventId = 0; // Safely reset it AFTER the delete is completely finished
                
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Error deleting event: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // 1. Check if an event was actually selected from the table
        if (selectedEventId == 0) {
            JOptionPane.showMessageDialog(this, "Please click on an event in the table first to update it.");
            return; // Stop the code
        }

        // 2. Grab the current data directly from the table's selected row
        DefaultTableModel model = (DefaultTableModel) tblEvents.getModel();
        int selectedRowIndex = tblEvents.getSelectedRow();

        String currentName = model.getValueAt(selectedRowIndex, 1).toString();
        String currentDate = model.getValueAt(selectedRowIndex, 2).toString();
        String currentLocation = model.getValueAt(selectedRowIndex, 3).toString();
        String currentSlots = model.getValueAt(selectedRowIndex, 4).toString();

        // 3. Create the text fields for the pop-up and PRE-FILL them with the current data
        javax.swing.JTextField txtName = new javax.swing.JTextField(currentName);
        javax.swing.JTextField txtDate = new javax.swing.JTextField(currentDate);
        javax.swing.JTextField txtLoc = new javax.swing.JTextField(currentLocation);
        javax.swing.JTextField txtSlots = new javax.swing.JTextField(currentSlots);

        // Bundle them up
        Object[] formFields = {
            "Edit Event Name:", txtName,
            "Edit Event Date (YYYY-MM-DD):", txtDate,
            "Edit Location:", txtLoc,
            "Edit Max Slots:", txtSlots
        };

        // 4. Trigger the pop-up window
        int result = JOptionPane.showConfirmDialog(this, formFields, "Update Event", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        // 5. If the user clicks "OK", save the new changes to the database
        if (result == JOptionPane.OK_OPTION) {
            try {
                Connection conn = DatabaseConnection.getConnection();

                // The SQL UPDATE query
                String sql = "UPDATE events SET event_name = ?, event_date = ?, venue = ?, max_slots = ? WHERE event_id = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);

                stmt.setString(1, txtName.getText());
                stmt.setString(2, txtDate.getText());
                stmt.setString(3, txtLoc.getText());
                stmt.setInt(4, Integer.parseInt(txtSlots.getText()));
                stmt.setInt(5, selectedEventId); // We use the hidden ID tracker for the WHERE clause!

                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Event Successfully Updated!");


                // Refresh the table and reset the tracker
                loadEventsTable();
                selectedEventId = 0;

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error updating event: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

