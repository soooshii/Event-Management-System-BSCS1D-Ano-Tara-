    private void loadEventsDropdown() {
        try {
            java.sql.Connection conn = DatabaseConnection.getConnection();
            
            String sql = "SELECT event_id, event_name FROM events";
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
            java.sql.ResultSet rs = stmt.executeQuery();
            
            cmbEvents.removeAllItems(); 
            eventMap.clear(); 
            
            while (rs.next()) {
                int id = rs.getInt("event_id");
                String name = rs.getString("event_name");
                
                cmbEvents.addItem(name); 
                eventMap.put(name, id);  
            }
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading dropdown: " + e.getMessage());
        }
    }
    
    private void loadAttendeesTable(int eventId) {
        try {
            // 1. Get the table model and wipe out the 4 ghost rows instantly
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblAttendees.getModel();
            model.setRowCount(0); 

            java.sql.Connection conn = DatabaseConnection.getConnection();
            
            // 2. Only grab attendees that match the currently selected event
            String sql = "SELECT * FROM registrations WHERE event_id = ?";
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, eventId);
            java.sql.ResultSet rs = stmt.executeQuery();
            
            // 3. Fill the table with the real data
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("registration_id"),
                    rs.getString("first_name"),
                    rs.getString("last_name"),
                    rs.getString("email"),
                    rs.getString("contact_number"),
                    rs.getString("attendance_status"),
                    rs.getTimestamp("registered_at")
                });
            }
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading roster: " + e.getMessage());
        }
    }
    
