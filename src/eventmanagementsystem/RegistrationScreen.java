    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       // 1. Safety Check: Make sure they didn't leave any boxes blank!
        if (txtFirstName.getText().trim().isEmpty() || 
            txtLastName.getText().trim().isEmpty() || 
            txtEmail.getText().trim().isEmpty() || 
            txtContact.getText().trim().isEmpty()) {
            
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all attendee details!");
            return; 
        }

        if (cmbEvents.getSelectedItem() == null) return;
        String selectedEventName = cmbEvents.getSelectedItem().toString();
        Integer eventId = eventMap.get(selectedEventName);

        if (eventId != null) {
            try {
                java.sql.Connection conn = DatabaseConnection.getConnection();
                
                // --- THE BOUNCER: SLOT ENFORCEMENT LOGIC ---
                
                // A. Find out the maximum slots allowed for this specific event
                java.sql.PreparedStatement maxStmt = conn.prepareStatement("SELECT max_slots FROM events WHERE event_id = ?");
                maxStmt.setInt(1, eventId);
                java.sql.ResultSet maxRs = maxStmt.executeQuery();
                int maxSlots = 0;
                if (maxRs.next()) {
                    maxSlots = maxRs.getInt("max_slots");
                }
                
                // B. Count how many people are currently registered
                java.sql.PreparedStatement countStmt = conn.prepareStatement("SELECT COUNT(*) AS total FROM registrations WHERE event_id = ?");
                countStmt.setInt(1, eventId);
                java.sql.ResultSet countRs = countStmt.executeQuery();
                int currentRegistrations = 0;
                if (countRs.next()) {
                    currentRegistrations = countRs.getInt("total");
                }
                
                // C. The strict cutoff!
                if (currentRegistrations >= maxSlots) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Registration Failed: This event is fully booked! (Limit: " + maxSlots + ")");
                    return; // Stop the code right here, do NOT save them to the database
                }
                
                // --- END OF THE BOUNCER ---

                // If they passed the bouncer, officially register them!
                String sql = "INSERT INTO registrations (event_id, first_name, last_name, email, contact_number, attendance_status) VALUES (?, ?, ?, ?, ?, ?)";
                java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
                
                stmt.setInt(1, eventId);
                stmt.setString(2, txtFirstName.getText().trim());
                stmt.setString(3, txtLastName.getText().trim());
                stmt.setString(4, txtEmail.getText().trim());
                stmt.setString(5, txtContact.getText().trim());
                stmt.setString(6, "Registered"); 
                
                stmt.executeUpdate();
                javax.swing.JOptionPane.showMessageDialog(this, "Attendee Successfully Registered!");
                
                // Wipe the text boxes clean
                txtFirstName.setText("");
                txtLastName.setText("");
                txtEmail.setText("");
                txtContact.setText("");
                
                // Refresh the table
                loadAttendeesTable(eventId);
                
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error registering attendee: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

